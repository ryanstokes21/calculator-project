function add(num1, num2) {
  return num1 + num2;
}

console.log(add(1, 3));

function subtract(num1, num2) {
  return num1 - num2;
}

console.log(subtract(3, 1));

function multiply(num1, num2) {
  return num1 * num2;
}

console.log(multiply(3, 2));

function divide(num1, num2) {
  return num1 / num2;
}

console.log(divide(4, 2));
